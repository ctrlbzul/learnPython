'''
In the program below, we take a temperature in degree Celsius
and convert it into degree Fahrenheit. They are related by the formula:
celsius * 1.8 = fahrenheit - 32
'''

# change this value for a different result
celcius = 37.5

# calculate fahrenheit
fahrenheit = (celcius * 1.8) + 32
print('%0.1f degree Celcius is equal to %0.1f degree Fahrenheit' %(celcius, fahrenheit))

