# logical operator and
password = "1234ab"
confirmPassword = "1234ab"

# result is true, only if both conditon is true
#print(password == "1234ab" and confirmPassword == "1234an") # False
print(password == "1234ab" and confirmPassword == "1234ab") # True

# logical operator or
# result is true, if one conditon is true
#print(password == "1234ab" and confirmPassword == "1234an") # true
print(password == "1234ab" and confirmPassword == "1234ab") # True
